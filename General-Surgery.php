<h2>General-Surgery</h2>
<p>The Department of General and Laparoscopic Surgery at Ivy is well equipped with the latest technology and infrastructure, supported by an experienced team of surgeons. We are committed to the principles and practices of WHO Global Initiative of Safe Surgery Save Lives. Our aim is to perform surgical procedures using minimal access techniques so that patients may enjoy faster recovery and fewer post-surgical complications.</p>

<p>"General Surgery" is a discipline of surgery having a central core of knowledge embracing anatomy, physiology, metabolism, immunology, nutrition, pathology, wound healing, shock and resuscitation, intensive care, and neoplasia, which are common to all surgical specialties.</p>

<p>A general surgeon has specialized knowledge and experience related to the diagnosis, preoperative, operative, and postoperative management, including the management of complications, in nine primary components of surgery, all of which are essential to the education of a broadly based surgeon:</p>

<p>Alimentary tract Abdomen and its contents Breast, skin, and soft tissue Head and neck, including trauma, vascular, endocrine, congenital and oncologic disorders—particularly tumors of the skin, salivary glands, thyroid, parathyroid, and the oral cavity
Vascular system, excluding the intracranial vessels and heart Endocrine system, including thyroid, parathyroid, adrenal, and endocrine pancreas
Surgical oncology, including coordinated multimodality management of the cancer patient by screening, surveillance, surgical adjunctive therapy, rehabilitation, and follow-up
Comprehensive management of trauma, including musculoskeletal, hand, and head injuries—the responsibility for all phases of care of the injured patient is an essential component of general surgery
Complete care of critically ill patients with underlying surgical conditions, in the emergency room, intensive care unit, and trauma/burn units
The training program must consist of at least five years.</p>
<p>
Not more than 12 months may be devoted to education in a single surgical discipline other than the principal components of surgery.
Not more than six months in total may be allocated to nonsurgical clinical disciplines such as internal medicine, pediatrics, gastroenterology, anesthesiology, or surgical pathology.
At least 54 months (4.5 years) must be clinical surgery, with experience in endoscopy, surgical intensive care, and emergency care included under this category.
Three of the clinical surgery years must be concerned with the principal components of general surgery.</p>
