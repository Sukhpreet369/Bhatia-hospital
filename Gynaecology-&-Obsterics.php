<h2>Gynaecology & Obsterics</h2>
<p>As a woman ages, many changes come in her body and different health issues develop at different stages. The Center for Obstetrics and Gynaecology at Bhatia hospital offer world-class and comprehensive care for women's health. Our expert Gynaecologists offer their best opinion on preventive measures, ensure accurate diagnosis and provide suitable treatment.</p>

<p>The team at the Center for Obstetrics and Gynaecology is known to deal with the most complex disorders or diseases related to the female reproductive tract. Our doctors are highly experienced and skilled to cater to women of different age groups – from teenagers to senior citizens.</p>
<p>The Department of Gynaecology and Obstetrics at Bhatia hospital supports women lifelong throughout their journey from childbearing age to menopause & beyond. We offer state-of-the-art technologies with supreme care offering a range of services such as pre-pregnancy, childbearing and menopausal care (Pre and Post). Against a backdrop of rapid developments, our department strives to provide our patients with the best care by keeping abreast of the fast changing panorama of women’s health.</p>

<p>Our multidisciplinary team of experienced clinicians and specialist nurses ensure that all our patients’ needs are addressed, providing our patients and their families with the support they need during their period of illness and recovery.</p>
