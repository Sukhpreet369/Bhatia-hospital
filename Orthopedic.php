<p>Open reduction and external fixation is a type of surgery used to fix broken bones</p>

<strong>Debridement</strong>
<p>It is the medical removal of dead, damaged or infected tissue to improve the healing potential of the remaining healthy tissue.</p>

<strong>Joint Replacement Surgery</strong>
<p>Joint replacement surgery is a procedure in which parts of a damaged joint are replaced with prosthesis. Joint replacement surgery is performed on the knee, hip, wrist, ankle, elbow and shoulder. The most commonly performed surgeries are hip and knee replacements.</p>

<strong>Total Knee Replacement Surgery</strong>
<p>Total Knee Replacement Surgery is performed to treat knee damage caused by arthritis or because of any injury due to which it may be difficult for the patient to perform daily routine activities like walking or climbing stairs. When non-surgical treatments like use of walking supports and medications prove no longer to be helpful, the doctor may advise the patient to undergo a total knee replacement surgery.</p>

<strong>Open Reduction Internal Fixation</strong>
