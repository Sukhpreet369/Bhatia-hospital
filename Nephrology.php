<strong>Kidney Biopsy</strong>
<p>A kidney biopsy involves taking two very tiny pieces (samples) of your kidney to look at with special microscopes to diagnose kidney diseases at early stages so that we can halt the damage to kidneys. The biopsy sample is taken by a kidney specialist through the skin with a very fine needle under USG guidance to increase precision. Its highly safe procedure takes just 2 minutes and we generally keep the patient for 4-6 hours in observation.</p>

<strong>Kidney Transplant Care</strong>
<p>Kidney Specialists or Nephrologists have a key role to play in kidney transplant care. It starts from the selection of suitable donors in the family to pre and post-op care of both donor and the recipient.</p>
