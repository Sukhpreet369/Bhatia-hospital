<h2>Treatment</h2>
<strong>Temporary Dialysis Catheter (Jugular, Femoral)</strong>
<p>It is a flexible, hollow tube inserted into a large vein in your neck, chest, or leg near the groin which allows the blood to flow into and out of your body. To decrease the pain and to avoid complications we are using USG guidance during its insertion. This is generally kept for a small period of 3-4 weeks.</p>

<strong>PermaCath Insertion</strong>
<p>(Tunneled Catheter) PermaCath tunneled Central Venous Catheter (CVC) is a special type of intravenous (IV) line that is placed into a large vein through a tunneled approach via chest. It can be used over a long period of time. We are doing its insertion in OT to maintain proper asepsis. The catheter will last anywhere from 6-12 months depending on its care.</p>

<strong>Hemodialysis</strong>
<p>Dialysis replaces some of the functions when your kidneys no longer work. In hemodialysis, a dialysis machine and a special filter called an artificial kidney, or a dialyzer, are used to clean your blood. We are using the latest and most sophisticated machines for better clearance and fewer complications. To get your blood into the dialyzer, the nephrologist always advises for the timely creation of access, or entrance, into your blood vessels known as AV fistula.</p>

