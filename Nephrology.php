<h2>Overview</h2>
<p>Nephrology concerns the diagnosis and treatment of kidney diseases, including electrolyte disturbances and hypertension, and the care of those requiring renal replacement therapy, including dialysis and renal transplant patients.The word 'dialysis' is from the mid-19th century: via Latin from the Greek word 'dialusis'; from 'dialuein' (split, separate), from 'dia' (apart) and 'luein' (set free). In other words, dialysis replaces the primary (excretory) function of the kidney, which separates (and removes) excess toxins and water from the blood, placing them in the urine.The mission of the Nephrology department at Bhatia hospital is to provide the highest quality of medical services to help patients suffering from various acute as well as chronic kidney diseases and kidney failure. The department provides all forms of dialysis including hemodialysis and peritoneal dialysis. It has an active renal transplant programme with around 100 transplants being performed every year. This is predominantly a living donor transplant programme.</p>
