<h2>Urology</h2>
<p>Urology is one of the fastest progressing fields which deals with the management of surgical problems of kidneys, ureter, bladder, prostate, seminal vesicles, and other genital organs like the testicles and penis in males. In the females, besides dealing with the management of surgical problems of kidneys, ureter, and bladder, it deals with surgical problems of urinary incontinence, the urinary fistula like the ureterovaginal and vesicovaginal communications.</p>
<p>Urology is a part of health care that deals with diseases of the male and female urinary tract (kidneys, ureters, bladder and urethra). It also deals with the male organs that are able to make babies (penis, testes, scrotum, prostate, etc.). Since health problems in these body parts can happen to everyone, urologic health is important.</p>
<p>Urology is known as a surgical specialty. Besides surgery, a urologist is a doctor with wisdom of internal medicine, pediatrics, gynecology and other parts of health care. This is because a urologist encounters a wide range of clinical problems. The scope of urology is big and the American Urological Association has named seven subspecialty parts:</p>
<p>
Pediatric Urology (children's urology)
Urologic Oncology (urologic cancers)
Renal (kidney) Transplant
Male Infertility
Calculi (urinary tract stones)
Female Urology
Neurourology (nervous system control of genitourinary organs)
Who takes care of urology patients?
If you have a problem with urologic health you might see a urologist. You might also see a person on the urologist's care team.</p>
</p>

