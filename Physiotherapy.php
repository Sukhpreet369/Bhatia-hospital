<h2>Physiotherapy</h2>
<p>Physiotherapy is treatment to restore, maintain, and make the most of a patient’s mobility, function, and well-being. Physiotherapy helps through physical rehabilitation, injury prevention, and health and fitness. Physiotherapists get you involved in your own recovery. </p>
<h2>When should I go see a physiotherapist?</h2>
<p>Think about getting physiotherapy if you have an injury, or chronic pain that affects how you function everyday. A doctor may refer you to physiotherapy after surgery such as a hip replacement, or an event such as a heart attack or stroke.</p>
<p>If you are planning to use health insurance to help cover the cost of physiotherapy, remember to visit your insurance company's website to ensure the physiotherapist is covered. If the physiotherapist is not covered by that insurance company you will not be able to use your benefits and will need to pay the full cost of treatment. </p>

<h2>What problems do physiotherapists treat?</h2>
<p>Physiotherapists focus on both prevention and rehabilitation. Treatment can be for problems caused by injury, disease or disability. Here are some examples:</p>

<div class="choose-left">
<ul class="list">
<li><i class="fa fa-caret-right"></i>Neck and back pain caused by problems in the muscles and skeleton</li>
<li><i class="fa fa-caret-right"></i>Problems in the bones, joints, muscles and ligaments, such as arthritis and the after-effects of amputation</li>
<li><i class="fa fa-caret-right"></i>Lung problems such as asthma</li>
<li><i class="fa fa-caret-right"></i>Disability as a result of heart problems</li>
<li><i class="fa fa-caret-right"></i>Pelvic issues, such as bladder and bowel problems related to childbirth</li>
<li><i class="fa fa-caret-right"></i>Loss of mobility because of trauma to the brain or spine, or due to diseases such as Parkinson’s disease and multiple sclerosis</li>
<li><i class="fa fa-caret-right"></i>Fatigue, pain, swelling, stiffness and loss of muscle strength, for example during cancer treatment, or palliative care </li>
</ul>
</div>
