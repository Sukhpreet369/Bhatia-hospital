<h2>Conditions and Treatments</h2>
<p><strong>RADICAL SURGERY</strong></p>
<p>Radical surgery is done for the removal of the tumor and its associated tissue for the cure of the patient.</p>

<p><strong>CONSERVATIVE CANCER SURGERY</strong></p>
<p>Surgery is done to conserve the form or function of the organ despite the removal of the whole tumor and some normal tissue around the tumor.</p>

<p><strong>CYTOREDUCTIVE SURGERY</strong></p>
<p>Removal of maximum amount of tumor or any nearby tissues to which cancer may have spread so that chemotherapy can eradicate the remaining cancer.</p>

<p><strong>METASTECTOMY</strong></p>
<p>Surgical removal of secondary cancerous growths that have originated from cancer from other parts of the body for relieving symptoms or complete cure of cancer.</p>


<p><strong>PROPHYLACTIC CANCER SURGERY</strong></p>
<p>Removal of a normal organ in order to decrease the chances of cancer in that tissue in later life in high-risk individuals</p>


<p><strong>PALLIATIVE SURGERY</strong></p>
<p>Surgery aimed to relieve the symptoms caused by cancer, so as to improve the quality of life of a patient when a complete cure for cancer is not possible.</p>

